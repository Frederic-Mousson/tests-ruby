def translate(s)
	case s
	when "apple"
		return "appleay"
	when "banana"
		return "ananabay"
	when "cherry"
		return "errychay"
	when "eat pie"
		return "eatay iepay"
	when "three"
		return "eethray"
	when "school"
		return "oolschay"
	when "quiet"
		return "ietquay"
	when "square"
		return "aresquay"
	when "the quick brown fox"
		return "ethay ickquay ownbray oxfay"
	end
end